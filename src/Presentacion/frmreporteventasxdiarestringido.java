     void mostrar(String buscar){
        try {
           
            logventa func = new logventa();
            modelo = func.mostrartablaventa(buscar);
            tablaventas.setModel(modelo);
            formatodetabla();
           lbltotal.setText("Total de ventas: "+ Integer.toString(  func.totalregistros));
           lbltotalimporte.setText("Importe total: $"+ Double.toString(func.Totalimporte)); 
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(rootPane,"Tienes un problema.. "+ e);
        }
    }
    

