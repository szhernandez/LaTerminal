    private void tablaventasMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaventasMouseClicked
        if (evt.getClickCount()==2 ) {
            try {
        //Abriendo ventana proveedores
         int fila= tablaventas.rowAtPoint(evt.getPoint());
        frmpuntoconsultaventa.idventa=tablaventas.getValueAt(fila, 0).toString();//asignando valor a numero de venta
        frmpuntoconsultaventa ventana = new frmpuntoconsultaventa();
        frminicio.escritorio.add(ventana);
        ventana.toFront();
        ventana.setMaximum(true);
        ventana.toFront();
      
        ventana.setVisible(true);  
      
       
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error: " + e.toString());
        }
      }//Fin if
    }//GEN-LAST:event_tablaventasMouseClicked

