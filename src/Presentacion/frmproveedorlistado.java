    private void btnnuevo1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnnuevo1ActionPerformed
        try {
            //Abriendo ventana trabajador

            frmproveedor proveedor = new frmproveedor();
            frminicio.escritorio.add(proveedor);
            proveedor.toFront();
            Dimension desktopSize = frminicio.escritorio.getSize();
            Dimension FrameSize = proveedor.getSize();
            proveedor.setLocation((desktopSize.width - FrameSize.width)/2, (desktopSize.height- FrameSize.height)/2);
            proveedor.toFront();
            proveedor.setVisible(true);

            //Enviando la accion que se requerira segun la funcion
            frmproveedor.lblaccion.setText("guardar");
            frmproveedor.btnaccion.setText("Guardar");
            frmproveedor.txtid.setVisible(false);
            frmproveedor.lblid.setVisible(false);
            frmproveedor.btneliminar.setVisible(false);
            this.dispose();
        } catch (Exception e) {
            JOptionPane.showConfirmDialog(null,"Error: " + e.toString());
        }
    }//GEN-LAST:event_btnnuevo1ActionPerformed

    private void tablaproveedoresMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaproveedoresMouseClicked
        //REFRESCANDO LA PAGINA CON UN CLIC EN TABLA
        if (evt.getClickCount()==1 ) {
            try {
                 mostrar(txtbuscar.getText());
            } catch (Exception e) {
            }
        }
        if (evt.getClickCount()==2 ) {
            try {
        //Abriendo ventana proveedores
        int fila= tablaproveedores.rowAtPoint(evt.getPoint());
        frmproveedor ventana = new frmproveedor();
        frminicio.escritorio.add(ventana);
        ventana.toFront();
        Dimension desktopSize = frminicio.escritorio.getSize();
        Dimension FrameSize = ventana.getSize();
        ventana.setLocation((desktopSize.width - FrameSize.width)/2, (desktopSize.height- FrameSize.height)/2);
        ventana.toFront();
        ventana.setVisible(true);  
       //Enviando la accion que se requerira segun la funcion
       frmproveedor.lblaccion.setText("editar");
       frmproveedor.btnaccion.setText("Editar");
       frmproveedor.txtid.setEnabled(false);
       frmproveedor.lblid.setEnabled(false);
       
       //Asignando datos a elementos ventana trabajador
       frmproveedor.txtid.setText(tablaproveedores.getValueAt(fila, 0).toString());
       frmproveedor.txtnombre.setText(tablaproveedores.getValueAt(fila, 1).toString());
       frmproveedor.txtdireccion.setText(tablaproveedores.getValueAt(fila, 2).toString());
       frmproveedor.txttelefono.setText(tablaproveedores.getValueAt(fila, 3).toString());
       frmproveedor.txttelefono2.setText(tablaproveedores.getValueAt(fila, 4).toString());
       frmproveedor.txtcorreo.setText(tablaproveedores.getValueAt(fila, 5).toString());
       frmproveedor.txtrepresentante.setText(tablaproveedores.getValueAt(fila, 6).toString());
      
      
       //Cerrando la ventana      
       //this.dispose();
       
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error: " + e.toString());
        }
      }//Fin if
    }//GEN-LAST:event_tablaproveedoresMouseClicked

